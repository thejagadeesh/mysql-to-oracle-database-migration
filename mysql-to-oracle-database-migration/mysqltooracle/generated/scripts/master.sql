CREATE OR REPLACE TRIGGER film_special_features_STRG
BEFORE INSERT OR UPDATE ON film
FOR EACH ROW
DECLARE
  input varchar2(500);
  val varchar2(50);
  cnt number := 1;
  num number := 0;
  str VARCHAR2(50);
  returnStr varchar2(500);
  setValue number :=0;
  type array_value is table of number index by varchar2(40);
  value_array array_value;
  type array_order is table of varchar2(40) index by BINARY_INTEGER;
  order_array array_order;
BEGIN
value_array('Trailers') := 0;
value_array('Commentaries') := 0;
value_array('Deleted Scenes') := 0;
value_array('Behind the Scenes') := 0;
order_array(0) :='Trailers';
order_array(1) :='Commentaries';
order_array(2) :='Deleted Scenes';
order_array(3) :='Behind the Scenes';

input := TRIM(:new.special_features);
LOOP
  val := getTokenizer(input,cnt);
  cnt := cnt + 1;
  if val is not null then 
     if value_array.exists(val) then
        value_array(val) := 1;
     end if;
  else
     exit;
  end if;
end loop;
num := order_array.FIRST;
LOOP
  EXIT WHEN NOT order_array.EXISTS(num);
  str := order_array(num);
  setValue := value_array(str);
  if (setValue = 1) then
    if returnStr is null then
      returnStr := str;
    else
      returnStr := returnStr || ',' || str;
    end if;
  end if;
  num := order_array.next(num);
END LOOP;

returnStr := substr(returnStr,0,length(returnStr));
if returnStr is not null then
  :new.special_features := returnStr;
else
  :new.special_features := ' ';
END IF;
END;

