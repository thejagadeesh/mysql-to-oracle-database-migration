CREATE OR REPLACE TRIGGER film_rating_ETRG
BEFORE INSERT OR UPDATE ON film
FOR EACH ROW
DECLARE
  v_val film.rating%TYPE;
  v_test film.rating%TYPE;
BEGIN
  v_val := null;
  v_test := TRIM(:new.rating);
  if v_test = '1' OR v_test = 'G' THEN 
  v_val := 'G';
  elsif v_test = '2' OR v_test = 'PG' THEN 
  v_val := 'PG';
  elsif v_test = '3' OR v_test = 'PG-13' THEN 
  v_val := 'PG-13';
  elsif v_test = '4' OR v_test = 'R' THEN 
  v_val := 'R';
  elsif v_test = '5' OR v_test = 'NC-17' THEN 
  v_val := 'NC-17';
  end if;  :new.rating := v_val;
END;
